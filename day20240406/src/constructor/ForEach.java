package constructor;

public class ForEach {
	public static void main(String[] args) {
		//for each 예제
		
		int[] numbers = {1, 2, 3, 4, 5};

		for (int num : numbers) {
		    System.out.println(num);
		}
		
		int[] prices = {2000,3000,4000,5000};
		int total = 0;
		for (int price : prices) {
			total += price;
			System.out.println(total);
		
		}
	}
}
